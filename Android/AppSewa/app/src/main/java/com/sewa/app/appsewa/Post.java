package com.sewa.app.appsewa;

public class Post {
    private int post_id;
    private String org_name;
    private int num_of_vols;
    private String cause;
    private String emergency_lvl;
    private String disaster_location;
    private String org_address;
    private String org_contact;
    private String org_email;
    private String num_of_reports;
    private String status;

    public int getPost_id() {
        return post_id;
    }

    public void setPost_id(int post_id) {
        this.post_id = post_id;
    }

    public String getOrg_name() {
        return org_name;
    }

    public void setOrg_name(String org_name) {
        this.org_name = org_name;
    }

    public int getNum_of_vols() {
        return num_of_vols;
    }

    public void setNum_of_vols(int num_of_vols) {
        this.num_of_vols = num_of_vols;
    }

    public String getCause() {
        return cause;
    }

    public void setCause(String cause) {
        this.cause = cause;
    }

    public String getEmergency_lvl() {
        return emergency_lvl;
    }

    public void setEmergency_lvl(String emergency_lvl) {
        this.emergency_lvl = emergency_lvl;
    }

    public String getDisaster_location() {
        return disaster_location;
    }

    public void setDisaster_location(String disaster_location) {
        this.disaster_location = disaster_location;
    }

    public String getOrg_address() {
        return org_address;
    }

    public void setOrg_address(String org_address) {
        this.org_address = org_address;
    }

    public String getOrg_contact() {
        return org_contact;
    }

    public void setOrg_contact(String org_contact) {
        this.org_contact = org_contact;
    }

    public String getOrg_email() {
        return org_email;
    }

    public void setOrg_email(String org_email) {
        this.org_email = org_email;
    }

    public String getNum_of_reports() {
        return num_of_reports;
    }

    public void setNum_of_reports(String num_of_reports) {
        this.num_of_reports = num_of_reports;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
